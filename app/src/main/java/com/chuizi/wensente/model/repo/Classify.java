public class Classify {
    private int code;
    private String desc;
    private String data;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    @Override
    public String toString() {
        return "Classify{" +
                "code=" + code +
                ", desc='" + desc + '\'' +
                ", data='" + data + '\'' +
                '}';
    }
}
